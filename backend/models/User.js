// Step 1: Require mongoose
const mongoose = require('mongoose');

// Step 2: Create a new schema
const User = new mongoose.Schema({
    firstName: {
        type: String,
        required: true,
    },
    lastName: {
        type: String,
        required: false,
    },
    email: {
        type: String,
        required: true,
    },
    username: {
        type: String,
        required: true,
    },
    likedSongs: {
        type: String,
        default: ""
    },
    likedPlaylists: {
        type: String,
        default: "",
    },
    subscribedArtists: {
        type: String,
        default: "",
    },
})

// Step 3: Create a model
const UserModel = mongoose.model('User', User);
module.exports = UserModel;